<h2>Hackspace Foundation report &mdash; June 2009</h2>

<h3>Introduction</h3>
<p>Our <a href="http://leeds.hackspace.org.uk/">friends in Leeds</a> have been particularly busy, 
racking up nearly £700 in donations for their space so far. You can now see this in the Liabilities section 
of the balance sheet. (For those unaccustomed to reading financial statements, this means that the 
amount of money available for spending on the London hack space is under the 
"Total Equity" category.)</p>

<p>The other big news this month was the <a href="http://news.bbc.co.uk/1/hi/magazine/8107803.stm">
BBC article</a> (which also got picked up by Wired). This 
drove a lot of new interest to our sites, although not a massive amount of new money.</p>

<p>We spent about £250 this month buying Arduinos from <a href="http://tinker.it">tinker.it</a>. These will hopefully be 
used for a forthcoming Arduino workshop, and also as stock to sell at a small 
markup.</p>

